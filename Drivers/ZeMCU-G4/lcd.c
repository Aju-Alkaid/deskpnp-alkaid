void LCD_ShowChar(uint16_t x, uint16_t y, char ch) {
    if (x > LCD_W - 8 || y > LCD_H - 16) return;
    uint8_t idx = ch - ' ';
    if (idx >= 95) return;
    for (uint8_t row = 0; row < 16; row++) {
        uint8_t line = asc2_1608[idx][row];
        for (uint8_t col = 0; col < 8; col++) {
            if (line & 0x01)
                ST7306_WritePixel(x + col, y + row, WHITE);
            else
                ST7306_WritePixel(x + col, y + row, BLACK);
            line >>= 1;
        }
    }
}

void LCD_ShowString(uint16_t x, uint16_t y, const char *str) {
    while (*str) {
        if (x > LCD_W - 8) { x = 0; y += 16; }
        if (y > LCD_H - 16) break;
        LCD_ShowChar(x, y, *str++);
        x += 8;
    }
}
